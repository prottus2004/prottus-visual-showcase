export type CertKind = "pdf" | "image";

export interface Certificate {
  /** Stable id for React keys. */
  id: string;
  /** Human-readable title (displayed on the card). */
  title: string;
  /** Issuer / category line shown beneath the title. */
  issuer: string;
  /** Issue date — ISO string or "YYYY" or free-form month/year. */
  date: string;
  /** File path under /public, e.g. "/certificates/foo.pdf". */
  file: string;
  /** pdf → render in iframe; image → render as <img>. */
  kind: CertKind;
  /** Short one-line takeaway shown on the card (optional). */
  blurb?: string;
  /** Hue in degrees, used to colour the card top-stripe. */
  hue: number;
  /** Optional lucide icon name resolved in the component. */
  icon: string;
}

